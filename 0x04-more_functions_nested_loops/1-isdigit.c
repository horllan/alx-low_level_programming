/**
 * _upper - starting point
 * Description: checks if a character is in uppercase
 * @c: value to check
 * Return: 1 if c is lowercase otherwise return 0
 */

int _isdigit(int c)
{
if (c >= '48' && c <= '57')
{
return (1);
}
else
{
return (0);
}
}
